// Generate dist/sw.js after the build. Offline strategy for a life-safety wiki:
//  - Precache: all crisis pages (EN + ES), the _astro asset bundle, and the
//    offline fallback — discovered by walking dist/, never hand-maintained.
//  - HTML is network-first: never serve a stale crisis number while online;
//    the cache is an offline fallback, not a speed hack.
//  - Cache version = hash of precached content, so a content fix ships a new
//    SW and re-precaches on the next visit.
// Runs after gen-index-redirects.mjs; skips its /index/ alias stubs.
import { readdirSync, statSync, readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { createHash } from 'node:crypto';

const DIST = new URL('../dist', import.meta.url).pathname;

/** Walk a dist subtree, returning page URLs (dirs with index.html), skipping alias stubs. */
function pageUrls(dir, url) {
  const urls = [];
  for (const name of readdirSync(dir)) {
    if (name === 'index') continue; // gen-index-redirects alias stubs
    const p = join(dir, name);
    if (statSync(p).isDirectory()) {
      urls.push(...pageUrls(p, `${url}/${name}`));
    } else if (name === 'index.html') {
      urls.push(`${url}/`);
    }
  }
  return urls;
}

const precache = [
  '/offline/', // directory-style like all pages: Pages 308s bare .html URLs, and
  // a redirect-tainted precache entry can't be served to navigations (Chrome)
  '/manifest.webmanifest',
  '/icon-192.png',
  ...pageUrls(join(DIST, 'crisis'), '/crisis'),
  ...pageUrls(join(DIST, 'es', 'crisis'), '/es/crisis'),
  ...readdirSync(join(DIST, '_astro')).map((f) => `/_astro/${f}`),
].sort();

// Version from precached content: a crisis-page fix → new hash → re-precache.
const hash = createHash('sha256');
for (const u of precache) {
  const file = u.endsWith('/') ? join(DIST, u, 'index.html') : join(DIST, u);
  hash.update(u).update(readFileSync(file));
}
const VERSION = hash.digest('hex').slice(0, 12);

const sw = `// generated by tools/gen-sw.mjs — do not edit
const CACHE = 'dw-${VERSION}';
const PRECACHE = ${JSON.stringify(precache, null, 0)};

self.addEventListener('install', (event) => {
  event.waitUntil((async () => {
    const cache = await caches.open(CACHE);
    await cache.addAll(PRECACHE);
    await self.skipWaiting();
  })());
});

self.addEventListener('activate', (event) => {
  event.waitUntil((async () => {
    for (const key of await caches.keys()) {
      if (key.startsWith('dw-') && key !== CACHE) await caches.delete(key);
    }
    await self.clients.claim();
  })());
});

// Match a navigation against the cache, tolerating a missing trailing slash.
async function matchPage(cache, request) {
  const hit = await cache.match(request, { ignoreSearch: true });
  if (hit) return hit;
  const url = new URL(request.url);
  if (!url.pathname.endsWith('/')) {
    return cache.match(url.origin + url.pathname + '/');
  }
}

// Cache a copy with the redirected flag stripped: Pages 308s no-slash URLs to
// trailing-slash, and browsers reject redirected responses served to navigations.
async function cachePut(cache, request, response) {
  const copy = response.redirected
    ? new Response(await response.clone().blob(), {
        status: response.status,
        statusText: response.statusText,
        headers: response.headers,
      })
    : response.clone();
  await cache.put(request, copy);
}

// Network-first with timeout: fresh content wins; cache is the offline fallback.
async function pageStrategy(request) {
  const cache = await caches.open(CACHE);
  try {
    const controller = new AbortController();
    const timer = setTimeout(() => controller.abort(), 5000);
    const response = await fetch(request, { signal: controller.signal });
    clearTimeout(timer);
    if (response.ok) await cachePut(cache, request, response);
    return response;
  } catch {
    return (await matchPage(cache, request)) || cache.match('/offline/');
  }
}

// Cache-first for content-hashed assets; stale-while-revalidate for the rest.
async function assetStrategy(request, hashed) {
  const cache = await caches.open(CACHE);
  const cached = await cache.match(request);
  if (cached && hashed) return cached; // hashed filename = immutable
  const refresh = fetch(request).then((response) => {
    if (response.ok) cache.put(request, response.clone());
    return response;
  });
  if (cached) {
    refresh.catch(() => {}); // revalidate in the background
    return cached;
  }
  return refresh;
}

self.addEventListener('fetch', (event) => {
  const request = event.request;
  if (request.method !== 'GET') return;
  const url = new URL(request.url);
  if (url.origin !== location.origin) return;
  if (request.mode === 'navigate') {
    event.respondWith(pageStrategy(request));
  } else {
    event.respondWith(assetStrategy(request, url.pathname.startsWith('/_astro/')));
  }
});
`;

writeFileSync(join(DIST, 'sw.js'), sw);
console.log(`sw.js: ${precache.length} precached URLs, version dw-${VERSION}`);
